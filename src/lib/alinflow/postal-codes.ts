function normalizeText(value?: string) {
  return String(value || "")
    .toLocaleLowerCase("hu-HU")
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .trim();
}

export function normalizePostalCodeInput(value?: string) {
  return String(value || "").replace(/\D/g, "").slice(0, 4);
}

function uniqueByPostalCity(items: PostalSettlement[]) {
  const seen = new Set<string>();
  return items.filter((item) => {
    const key = `${item.postalCode}-${normalizeText(item.city)}`;
    if (seen.has(key)) return false;
    seen.add(key);
    return true;
  });
}

export function settlementLabel(item: PostalSettlement) {
  return `${item.postalCode} ${item.city}`;
}

export function postalCodeMatches(value?: string, limit = 8) {
  const postalCode = normalizePostalCodeInput(value);
  if (postalCode.length < 2) return [];
  return uniqueByPostalCity(POSTAL_SETTLEMENTS.filter((item) => item.postalCode.startsWith(postalCode))).slice(0, limit);
}

export function cityMatches(value?: string, limit = 8) {
  const needle = normalizeText(value);
  if (needle.length < 2) return [];

  const exact = POSTAL_SETTLEMENTS.filter((item) => normalizeText(item.city) === needle);
  const starts = POSTAL_SETTLEMENTS.filter((item) => normalizeText(item.city).startsWith(needle) && normalizeText(item.city) !== needle);
  const contains = POSTAL_SETTLEMENTS.filter((item) => {
    const city = normalizeText(item.city);
    return city.includes(needle) && !city.startsWith(needle);
  });

  return uniqueByPostalCity([...exact, ...starts, ...contains]).slice(0, limit);
}

export function exactPostalCodeSettlements(value?: string) {
  const postalCode = normalizePostalCodeInput(value);
  if (postalCode.length !== 4) return [];
  return uniqueByPostalCity(POSTAL_SETTLEMENTS.filter((item) => item.postalCode === postalCode));
}

export function exactCitySettlements(value?: string) {
  const needle = normalizeText(value);
  if (!needle) return [];
  return uniqueByPostalCity(POSTAL_SETTLEMENTS.filter((item) => normalizeText(item.city) === needle));
}

export function uniqueSettlementByPostalCode(value?: string) {
  const matches = exactPostalCodeSettlements(value);
  return matches.length === 1 ? matches[0] : undefined;
}

export function uniqueSettlementByCity(value?: string) {
  const matches = exactCitySettlements(value);
  return matches.length === 1 ? matches[0] : undefined;
}

export function formatPostalCity(postalCode?: string, city?: string, fallback = "nincs település") {
  const code = normalizePostalCodeInput(postalCode);
  const cleanCity = String(city || "").trim();
  const text = [code, cleanCity].filter(Boolean).join(" ");
  return text || fallback;
}
